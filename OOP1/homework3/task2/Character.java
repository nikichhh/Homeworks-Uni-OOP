package bg.tu_varna.sit.b2.f23621689.homework3.task2;

public class Character {
    private String name;
    private int life;

    public Character(String name, int life) {
        this.name = name;
        this.life = life;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }
}
