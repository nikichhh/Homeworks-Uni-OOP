package bg.tu_varna.sit.b2.f23621689.homework3.task3;

public class City {
    private String name;
    private int postalCode;

    public City(String name, int postalCode) {
        this.name = name;
        this.postalCode = postalCode;
    }

    public String getName() {
        return name;
    }

    public int getPostalCode() {
        return postalCode;
    }
}
